package com.bit.sfa.Models;

public class Reference {
	
	private String charVal;
	private String dealPreFix;
	private String repPrefix;
	
	public String getCharVal() {
		return charVal;
	}
	public void setCharVal(String charVal) {
		this.charVal = charVal;
	}
	public String getDealPreFix() {
		return dealPreFix;
	}
	public void setDealPreFix(String dealPreFix) {
		this.dealPreFix = dealPreFix;
	}
	public String getRepPrefix() {
		return repPrefix;
	}
	public void setRepPrefix(String repPrefix) {
		this.repPrefix = repPrefix;
	}
	
	
}
